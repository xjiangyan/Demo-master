    public static void focusOnTouch(MotionEvent event, Camera camera, SurfaceView surfaceView) {
        if (camera != null) {
            //            mCamera.cancelAutoFocus();
            Rect focusRect = calculateTapArea(event.getX(), event.getY(), 1f, surfaceView);
            Rect meteringRect = calculateTapArea(event.getX(), event.getY(), 1.5f, surfaceView);

            Camera.Parameters parameters = camera.getParameters();
            parameters.setFocusMode(Camera.Parameters.FOCUS_MODE_AUTO);
            List<Camera.Area> areas = new ArrayList<>();
            areas.add(new Camera.Area(focusRect, 1000));
            parameters.setFocusAreas(areas);

            if (parameters.getMaxNumMeteringAreas() > 0) {
                List<Camera.Area> meteringAreas = new ArrayList<>();
                meteringAreas.add(new Camera.Area(meteringRect, 1000));
                parameters.setMeteringAreas(meteringAreas);
            }

            camera.setParameters(parameters);
            camera.autoFocus(new Camera.AutoFocusCallback() {
                @Override
                public void onAutoFocus(boolean success, Camera camera) {

                }
            });
        }
    }
    private static Rect calculateTapArea(float x, float y, float coefficient, SurfaceView surfaceView) {
        int FOCUS_AREA_SIZE = 300;
        int areaSize = Float.valueOf(FOCUS_AREA_SIZE * coefficient).intValue();

        int centerX = (int) (x / surfaceView.getWidth() * 2000 - 1000);
        int centerY = (int) (y / surfaceView.getHeight() * 2000 - 1000);

        int left = clamp(centerX - areaSize / 2, -1000, 1000);
        int right = clamp(left + areaSize, -1000, 1000);
        int top = clamp(centerY - areaSize / 2, -1000, 1000);
        int bottom = clamp(top + areaSize, -1000, 1000);

        return new Rect(left, top, right, bottom);
    }

    private static int clamp(int x, int min, int max) {
        if (x > max) {
            return max;
        }
        if (x < min) {
            return min;
        }
        return x;
    }

